    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtStudentId, sb, "Mã sinh viên cần phải nhập");
        DataValidator.validateEmpty(txtEnglish, sb, "Tiếng Anh cần phải nhập");
        DataValidator.validateEmpty(txtIT, sb, "Tin Học cần phải nhập");
        DataValidator.validateEmpty(txtPhysicalTraining, sb, "Giáo dục thể chất cần phải nhập");
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            
            BangDiem bd = new BangDiem();
            
            bd.setMaSV(txtStudentId.getText());
            bd.setTiengAnh(Float.parseFloat(txtEnglish.getText()));
            bd.setTinHoc(Float.parseFloat(txtIT.getText()));
            bd.setGDTC(Float.parseFloat(txtPhysicalTraining.getText()));
            
            BangDiemDao dao = new BangDiemDao();
            if (dao.findByMaSV(txtStudentId.getText()) != null){
                if (MessageDialogHelper.showConfirmDialog(parentForm,
                        "Bạn có muốn cập nhập điểm không?", "Hỏi") == JOptionPane.NO_OPTION){
                    return;
                }
                if (dao.update(bd)){
                MessageDialogHelper.showMessageDialog(parentForm, "Bảng điểm đã được cập nhập", "Thông báo");
                }else{
                MessageDialogHelper.showMessageDialog(parentForm, "Không thể cập nhập bảng điểm", "Thông báo");
            }
        }else{
                           
            if (dao.insert(bd)){
                MessageDialogHelper.showMessageDialog(parentForm, "Bảng điểm đã được lưu", "Thông báo");
            }else{
                MessageDialogHelper.showMessageDialog(parentForm, "Không thể lưu bảng điểm", "Thông báo");
            }
        }
            loadBangDiem();
        } catch (Exception e) {
            e.printStackTrace();
//            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
              MessageDialogHelper.showErrorDialog(parentForm, "Điểm thành phần nhập sai định dạng", "Lỗi");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

